def gps(mapa: Matrix[str], target: str) -> int:
    return sum(100*x + y for x, y in where2(mapa == target))


def process_data(data: str) -> tuple[Matrix[str], str]:
    """transform the raw data into a processable form"""
    mapa_raw, moves = ir.isplit(map(str.strip, data.splitlines()))
    mapa = numpy.array([list(line) for line in mapa_raw], dtype=str)
    return mapa, "".join(moves)


def get_raw_data(path: str = "./input.txt") -> str:
    with open(path) as file:
        return file.read()
